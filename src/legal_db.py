def get_relevant_laws(letter_type: str, country: str = 'de') -> Dict:
    """
    Отримати закони для типу листа та країни.
    
    Args:
        letter_type: Тип листа (debt_collection, tenancy, employment, administrative, general)
        country: Код країни ('de', 'us', тощо)
    
    Returns:
        Словник з законами та наслідками
    """
    country_db = LEGAL_DB.get(country, LEGAL_DB['de'])
    return country_db.get(letter_type, country_db['general'])

